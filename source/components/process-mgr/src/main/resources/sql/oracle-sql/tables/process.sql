CREATE TABLE SAIX_AUTH.PROCESS (
	PROCESSSEQID NUMBER(20) GENERATED BY DEFAULT ON NULL AS IDENTITY,
	PROCESSNAME VARCHAR2(256) NOT NULL,
	HOSTNAME VARCHAR2(256) NOT NULL,
	STATUS VARCHAR2(64) NOT NULL,
	STARTTIME TIMESTAMP WITH time zone NOT NULL,
	LASTHEARTBEATTIME TIMESTAMP WITH time zone NOT NULL,
	NUMBEROFHEARTBEAT NUMBER(10),
	ACTIVESTARTTIME TIMESTAMP WITH time zone,
	ENDTIME TIMESTAMP WITH time zone,
	OSPID VARCHAR2(100),
	APPLICATIONVERSION VARCHAR2(256),
	PROCESSMGRVERSION VARCHAR2(256),
	NOTES VARCHAR2(256),
	PRIMARY KEY (PROCESSSEQID),
	FOREIGN KEY (STATUS)
      REFERENCES SAIX_AUTH.PROCESSSTATUS (STATUS)
);

COMMENT ON TABLE SAIX_AUTH.PROCESS IS 'Table to monitor the process at center location.';
COMMENT ON COLUMN SAIX_AUTH.PROCESS.PROCESSSEQID IS 'Unique process id generated by system';
COMMENT ON COLUMN SAIX_AUTH.PROCESS.PROCESSNAME IS 'Name of process/thread/engine to monitor';
COMMENT ON COLUMN SAIX_AUTH.PROCESS.HOSTNAME IS 'Host where this process is running';
COMMENT ON COLUMN SAIX_AUTH.PROCESS.STATUS IS 'Current process status';
COMMENT ON COLUMN SAIX_AUTH.PROCESS.STARTTIME IS 'Time when this process is started.';
COMMENT ON COLUMN SAIX_AUTH.PROCESS.LASTHEARTBEATTIME IS 'Time when last heart beat received for this process';
COMMENT ON COLUMN SAIX_AUTH.PROCESS.NUMBEROFHEARTBEAT IS 'Number of time heart beat send from this process';
COMMENT ON COLUMN SAIX_AUTH.PROCESS.ACTIVESTARTTIME IS 'Time when this process is becomes active and started processing.';
COMMENT ON COLUMN SAIX_AUTH.PROCESS.ENDTIME IS 'Time when this process is EXITED, KILLED or ERROR out';
COMMENT ON COLUMN SAIX_AUTH.PROCESS.OSPID IS 'OS Process Id';
COMMENT ON COLUMN SAIX_AUTH.PROCESS.APPLICATIONVERSION IS 'Application version running this process';
COMMENT ON COLUMN SAIX_AUTH.PROCESS.PROCESSMGRVERSION IS 'Process manager version managing this process';
COMMENT ON COLUMN SAIX_AUTH.PROCESS.NOTES IS 'Notes for this process, e.g. host abc killed this process';
